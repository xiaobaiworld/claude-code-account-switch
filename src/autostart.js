// Windows 任务计划程序集成：注册一个登录时触发的任务，执行 `ccs monitor revive`。
// 设计要点：
//   - 仅 Windows 支持。其他平台静默 noop（返回 supported:false）。
//   - 任务以"当前用户、仅交互登录触发"方式运行，不需要管理员（SYSTEM 身份读不到 ~/.claude 凭证，不可用）。
//   - 用 ~/.ccs/autostart-revive.bat 做胶水层：schtasks 的 /TR 引号转义在 Node spawn 下
//     反复踩坑（实测带空格路径会被存成字面 \"...\"，运行时返回 ERROR_FILE_NOT_FOUND），
//     胶水脚本一次写好就稳定。任务计划只调用这个 .bat，命令本体集中在脚本里。
//   - 读 schtasks 输出强制 GBK 解码（中文 Windows 默认 OEM=936），否则字段名乱码无法解析。
const fs = require('fs');
const path = require('path');
const { spawnSync } = require('child_process');
const { CCS_DIR, ensureDir } = require('./utils');

const TASK_NAME = 'CCS-UsageMonitor';
const IS_WINDOWS = process.platform === 'win32';
const BAT_PATH = path.join(CCS_DIR, 'autostart-revive.bat');

function _ccsJsPath() {
  return path.join(__dirname, '..', 'bin', 'ccs.js');
}

function _runSchtasks(args) {
  // 通过 cmd.exe /c "chcp 65001 >nul && schtasks ..." 让 schtasks 输出 UTF-8。
  // 中文 Windows 默认 OEM=936（GBK），不切代码页时字段名乱码无法解析。
  const cmdLine = `chcp 65001 >nul && schtasks.exe ${args.map(_quoteArg).join(' ')}`;
  const r = spawnSync('cmd.exe', ['/d', '/s', '/c', cmdLine], { windowsHide: true });
  return {
    code: r.status,
    stdout: r.stdout ? r.stdout.toString('utf8') : '',
    stderr: r.stderr ? r.stderr.toString('utf8') : '',
  };
}

function _quoteArg(a) {
  if (a === '' || /[\s"]/.test(a)) return `"${a.replace(/"/g, '""')}"`;
  return a;
}

function _writeBatHelper() {
  ensureDir(CCS_DIR);
  const node = process.execPath;
  const ccs  = _ccsJsPath();
  // .bat 里直接用双引号包路径就行，没有 spawn 的二次包装
  const content =
    '@echo off\r\n' +
    'REM Auto-generated by ccs autostart install. Do not edit; reinstall to regenerate.\r\n' +
    `"${node}" "${ccs}" monitor revive\r\n`;
  fs.writeFileSync(BAT_PATH, content, { encoding: 'utf8' });
}

function isSupported() {
  return IS_WINDOWS;
}

// 已安装则返回 { installed:true, ... }；未安装返回 { installed:false }
function status() {
  if (!IS_WINDOWS) return { supported: false, installed: false };
  // 用 /V 才会输出 "Task To Run" / "Last Result" 等字段
  const r = _runSchtasks(['/Query', '/TN', TASK_NAME, '/FO', 'LIST', '/V']);
  if (r.code !== 0) return { supported: true, installed: false };
  const lines = r.stdout.split(/\r?\n/);
  const findLine = (re) => {
    for (const l of lines) {
      const m = l.match(re);
      if (m) return m[1].trim();
    }
    return null;
  };
  // 中文 Windows 字段名取决于系统语言；英文环境字段名带不定数量空格
  const taskToRun = findLine(/^(?:Task To Run|要运行的任务)\s*:\s*(.+)$/i);
  const lastRun   = findLine(/^(?:Last Run Time|上次运行时间)\s*:\s*(.+)$/i);
  const nextRun   = findLine(/^(?:Next Run Time|下次运行时间)\s*:\s*(.+)$/i);
  const statusStr = findLine(/^(?:Status|状态)\s*:\s*(.+)$/i);
  const lastResult= findLine(/^(?:Last Result|上次结果)\s*:\s*(.+)$/i);
  return { supported: true, installed: true, taskToRun, lastRun, nextRun, status: statusStr, lastResult };
}

function install() {
  if (!IS_WINDOWS) return { supported: false, installed: false, message: '当前平台不支持任务计划程序（仅 Windows）' };
  _writeBatHelper();
  _runSchtasks(['/Delete', '/TN', TASK_NAME, '/F']);
  // /DELAY 0001:00 = 登录后延迟 1 分钟再触发，给网络/DNS/VPN/凭证刷新留出就绪时间。
  // 旧版无延迟导致登录后 1 秒就开始查 API，必失败，然后 60→120→240→300s 退避 11 分钟才恢复。
  const r = _runSchtasks([
    '/Create',
    '/TN', TASK_NAME,
    '/SC', 'ONLOGON',
    '/DELAY', '0001:00',
    '/RL', 'LIMITED',
    '/F',
    '/TR', BAT_PATH,
  ]);
  if (r.code !== 0) {
    const msg = (r.stderr || r.stdout || '').trim() || `schtasks exit ${r.code}`;
    return { supported: true, installed: false, ok: false, error: msg };
  }
  return { supported: true, installed: true, ok: true, batPath: BAT_PATH };
}

function remove() {
  if (!IS_WINDOWS) return { supported: false, removed: false };
  try { fs.unlinkSync(BAT_PATH); } catch { /* 没有就算了 */ }
  const r = _runSchtasks(['/Delete', '/TN', TASK_NAME, '/F']);
  if (r.code !== 0) {
    // 任务本来就不存在也算成功
    const out = (r.stderr || r.stdout || '').toLowerCase();
    if (out.includes('cannot find') || out.includes('找不到') || out.includes('does not exist')) {
      return { supported: true, removed: false, alreadyAbsent: true };
    }
    return { supported: true, removed: false, ok: false, error: (r.stderr || r.stdout || '').trim() };
  }
  return { supported: true, removed: true, ok: true };
}

module.exports = { isSupported, status, install, remove, TASK_NAME };
